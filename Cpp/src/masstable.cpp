// Returns list of mass numbers available for element Z
// ====================================================================

void GETAS(int IZ, int& NUMAS, int* IAS) {
    NUMAS = 0;
    int IZZ = IZ + 1;
    if (IZZ < 1 || IZZ > IZMAX) return;
    int LZ = LZLO[IZZ] - 1;
    NUMAS = LZHI[IZZ] - LZ;
    for (int I = 1; I <= NUMAS; I++) {
        IAS[I] = (int)(ABUF(1, LZ + I) + 0.5);
    }
    return;
}

